<h3 class="text-xl font-bold text-blue-800 mb-2">A. Pengertian Tag Semantik</h3>
<p class="mb-4 text-justify">
    Tag semantik adalah tag HTML yang memiliki makna jelas tentang isi di dalamnya, baik untuk browser, developer, maupun mesin pencari. Berbeda dengan tag non-semantik (seperti <code>&lt;div&gt;</code>) yang hanya berfungsi sebagai pembungkus, tag semantik menjelaskan struktur konten halaman web.
</p>

<div class="bg-blue-50 p-4 rounded-lg border-l-4 border-blue-500 mb-6">
    <strong class="text-blue-800">ðŸ’¡ Contoh:</strong>
    Tag <code>&lt;header&gt;</code> jelas menunjukkan bagian kepala, sedangkan <code>&lt;footer&gt;</code> menunjukkan bagian kaki halaman.
</div>

<h3 class="text-xl font-bold text-blue-800 mb-2">B. Fungsi & Manfaat</h3>
<ul class="list-disc pl-5 mb-6 space-y-2 text-gray-700">
    <li>Membuat struktur halaman web lebih rapi dan terorganisir.</li>
    <li>Memudahkan developer dalam membaca dan mengelola kode.</li>
    <li>Membantu mesin pencari (SEO) memahami isi halaman web.</li>
    <li>Meningkatkan aksesibilitas, terutama bagi pengguna pembaca layar (screen reader).</li>
</ul>

<h3 class="text-xl font-bold text-blue-800 mb-2">C. Jenis Tag Semantik & Fungsinya</h3>
<div class="space-y-4">
    <div class="border border-gray-200 rounded-lg p-4 hover:shadow-md transition">
        <code class="text-red-600 font-bold bg-red-50 px-2 py-1 rounded">&lt;header&gt;</code>
        <p class="mt-2 text-sm text-gray-600">Bagian kepala halaman, biasanya berisi judul, logo, atau menu navigasi.</p>
    </div>

    <div class="border border-gray-200 rounded-lg p-4 hover:shadow-md transition">
        <code class="text-red-600 font-bold bg-red-50 px-2 py-1 rounded">&lt;nav&gt;</code>
        <p class="mt-2 text-sm text-gray-600">Menu navigasi, seperti link Home, About, Contact.</p>
    </div>

    <div class="border border-gray-200 rounded-lg p-4 hover:shadow-md transition">
        <code class="text-red-600 font-bold bg-red-50 px-2 py-1 rounded">&lt;main&gt;</code>
        <p class="mt-2 text-sm text-gray-600">Konten utama halaman. Hanya boleh ada satu tag main per halaman.</p>
    </div>

    <div class="border border-gray-200 rounded-lg p-4 hover:shadow-md transition">
        <code class="text-red-600 font-bold bg-red-50 px-2 py-1 rounded">&lt;article&gt;</code>
        <p class="mt-2 text-sm text-gray-600">Konten yang berdiri sendiri, seperti artikel berita atau posting blog.</p>
    </div>

    <div class="border border-gray-200 rounded-lg p-4 hover:shadow-md transition">
        <code class="text-red-600 font-bold bg-red-50 px-2 py-1 rounded">&lt;aside&gt;</code>
        <p class="mt-2 text-sm text-gray-600">Konten sampingan, seperti sidebar atau iklan.</p>
    </div>

    <div class="border border-gray-200 rounded-lg p-4 hover:shadow-md transition">
        <code class="text-red-600 font-bold bg-red-50 px-2 py-1 rounded">&lt;footer&gt;</code>
        <p class="mt-2 text-sm text-gray-600">Bagian penutup halaman, berisi hak cipta atau kontak.</p>
    </div>
</div>